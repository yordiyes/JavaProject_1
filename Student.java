// The Student class represents the information associated with a student in the registration system
public class Student { 
    // Static fields to store information about the student
    static String fullName;        // Full name of the student
    static int studentId;          // Student ID
    static String department;      // Department of the student
    static int studentYear;        // Batch year of the student
    static String gender;          // Gender of the student
    static String modality;        // Modality of the student's registration
    
}
